<?php

namespace App\MyClass;

abstract class pelayananPublik implements antarmuka
{
    protected $name;

    public function __construct($name)
    {
        $this->name = $name;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    abstract public function salamDariBinjai();
}
